let currentCategory = 'all';

// Рендер карточек
function renderMenu(category) {
    const grid = document.getElementById('menuGrid');
    let items = [];
    
    if (category === 'all') {
        Object.values(menuData).forEach(cat => items.push(...cat));
    } else {
        items = menuData[category] || [];
    }
    
    grid.innerHTML = items.map((item, idx) => `
        <div class="menu-card" onclick="openModal('${category}', ${idx})">
            <div class="card-name">
                ${item.name}
                ${item.badge ? `<span class="card-badge ${item.badge === 'PREMIUM' ? 'premium' : ''}">${item.badge}</span>` : ''}
            </div>
            <div class="card-meta">
                <span class="card-volume">${item.volume}</span>
                <span class="card-price">${item.price}</span>
            </div>
            <div class="card-hint">👆 Нажмите для подробностей</div>
        </div>
    `).join('');
}

// Открытие модального окна
function openModal(category, idx) {
    const items = category === 'all' ? 
        Object.values(menuData).flat() : 
        menuData[category];
    
    const item = items[idx];
    if (!item) return;
    
    document.getElementById('modalTitle').textContent = item.name;
    document.getElementById('modalVolume').textContent = `📏 ${item.volume}`;
    document.getElementById('modalPrice').textContent = `💰 ${item.price}`;
    
    let bodyHTML = '';
    
    if (item.desc) {
        bodyHTML += `<div class="modal-section"><h3> Описание</h3><p>${item.desc}</p></div>`;
    }
    
    if (item.ingredients && item.ingredients.length) {
        bodyHTML += `<div class="modal-section"><h3>🧪 Состав</h3><ul>${item.ingredients.map(i => `<li>${i}</li>`).join('')}</ul></div>`;
    }
    
    if (item.method) {
        bodyHTML += `<div class="modal-section"><h3>‍🍳 Технология</h3><p>${item.method}</p></div>`;
    }
    
    if (item.history) {
        bodyHTML += `<div class="modal-section"><h3>📚 История</h3><p>${item.history}</p></div>`;
    }
    
    document.getElementById('modalBody').innerHTML = bodyHTML;
    document.getElementById('modal').classList.add('active');
    document.body.style.overflow = 'hidden';
}

// Закрытие модального окна
function closeModal() {
    document.getElementById('modal').classList.remove('active');
    document.body.style.overflow = '';
}

